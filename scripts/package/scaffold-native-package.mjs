#!/usr/bin/env node

import fs from "node:fs";
import path from "node:path";

const ROOT = process.cwd();
const TEMPLATE_DIR = path.join(ROOT, "scripts/package/templates");
const OUT_DIR = path.join(ROOT, ".package-scaffold");

const FILES = [
  ["capacitor.config.example.json", "capacitor.config.json"],
  ["tauri.conf.example.json", "src-tauri/tauri.conf.json"],
  ["electron-main.example.cjs", "electron/main.cjs"],
  ["electron-preload.example.cjs", "electron/preload.cjs"]
];

function ensureDir(dirPath) {
  fs.mkdirSync(dirPath, { recursive: true });
}

function copyTemplate(sourceName, targetName, force) {
  const sourcePath = path.join(TEMPLATE_DIR, sourceName);
  const targetPath = path.join(OUT_DIR, targetName);

  ensureDir(path.dirname(targetPath));

  if (fs.existsSync(targetPath) && !force) {
    return {
      path: path.relative(ROOT, targetPath),
      created: false,
      skipped: true
    };
  }

  fs.copyFileSync(sourcePath, targetPath);

  return {
    path: path.relative(ROOT, targetPath),
    created: true,
    skipped: false
  };
}

function createReadme() {
  const readmePath = path.join(OUT_DIR, "README.md");

  if (fs.existsSync(readmePath)) {
    return {
      path: path.relative(ROOT, readmePath),
      created: false,
      skipped: true
    };
  }

  fs.writeFileSync(
    readmePath,
    [
      "# Native Package Scaffold",
      "",
      "Generated by `npm run package:scaffold`.",
      "",
      "This scaffold intentionally lives outside source control. Pick one native shell first:",
      "",
      "- Capacitor for mobile packaging.",
      "- Tauri for lightweight desktop packaging.",
      "- Electron when Node-backed desktop integrations are required.",
      "",
      "Keep the Next.js app as the source of truth. Native shells should wrap the built Web/PWA output instead of forking UI code.",
      ""
    ].join("\n"),
    "utf8"
  );

  return {
    path: path.relative(ROOT, readmePath),
    created: true,
    skipped: false
  };
}

function createReport(force) {
  ensureDir(OUT_DIR);

  return {
    ok: true,
    outDir: path.relative(ROOT, OUT_DIR),
    files: [...FILES.map(([source, target]) => copyTemplate(source, target, force)), createReadme()]
  };
}

function printReport(report) {
  console.log("Learn English native package scaffold");
  console.log("");
  console.log(`Output: ${report.outDir}`);
  report.files.forEach((file) => {
    console.log(`${file.created ? "created" : "exists"} ${file.path}`);
  });
}

const args = new Set(process.argv.slice(2));
const report = createReport(args.has("--force"));

if (args.has("--json")) {
  console.log(JSON.stringify(report, null, 2));
} else {
  printReport(report);
}
