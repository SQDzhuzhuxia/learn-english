#!/usr/bin/env node

import fs from "node:fs";
import path from "node:path";
import { spawn } from "node:child_process";

const ROOT = process.cwd();
const MANIFEST_PATH = path.join(ROOT, "scripts/local-speech/models-manifest.json");
const OUT_DIR = path.join(ROOT, ".local-speech");

function readManifest() {
  return JSON.parse(fs.readFileSync(MANIFEST_PATH, "utf8"));
}

function parseArgs() {
  const args = process.argv.slice(2);
  const options = {
    json: args.includes("--json"),
    write: args.includes("--write"),
    run: args.includes("--run"),
    service: undefined
  };
  const serviceIndex = args.indexOf("--service");

  if (serviceIndex >= 0 && args[serviceIndex + 1]) {
    options.service = args[serviceIndex + 1];
  }

  return options;
}

function resolveServices(manifest, options) {
  return manifest.services.filter((service) => !options.service || service.id === options.service);
}

function buildPowerShellScript(services) {
  const lines = [
    "# Generated by npm run speech:start -- --write",
    "# Review commands before running in a production environment.",
    "$ErrorActionPreference = 'Stop'",
    ""
  ];

  services.forEach((service) => {
    lines.push(`# ${service.label}`);
    Object.entries(service.env).forEach(([key, value]) => {
      lines.push(`$env:${key} = '${String(value).replaceAll("'", "''")}'`);
    });

    if (service.runtime?.command) {
      const args = (service.runtime.args ?? [])
        .map((arg) => String(arg).replaceAll("'", "''"))
        .map((arg) => `'${arg}'`)
        .join(", ");
      lines.push(`Start-Process -FilePath '${service.runtime.command}' -ArgumentList @(${args}) -WindowStyle Hidden`);
    } else {
      lines.push(`Write-Host 'Manual start required: ${service.startCommand.replaceAll("'", "''")}'`);
    }

    lines.push("");
  });

  lines.push("Write-Host 'Run npm run speech:check -- --strict-practice after services start.'");
  return lines.join("\n");
}

function writeStartScript(services) {
  fs.mkdirSync(OUT_DIR, { recursive: true });
  const filePath = path.join(OUT_DIR, "start-local-speech.ps1");
  fs.writeFileSync(filePath, buildPowerShellScript(services), "utf8");
  return path.relative(ROOT, filePath);
}

function runService(service) {
  if (!service.runtime?.command) {
    return {
      id: service.id,
      action: "manual",
      detail: service.startCommand
    };
  }

  const child = spawn(service.runtime.command, service.runtime.args ?? [], {
    cwd: ROOT,
    detached: true,
    stdio: "ignore",
    windowsHide: true,
    env: {
      ...process.env,
      ...service.env
    }
  });

  child.unref();

  return {
    id: service.id,
    action: "started",
    detail: `${service.runtime.command} ${(service.runtime.args ?? []).join(" ")}`.trim()
  };
}

function createReport() {
  const manifest = readManifest();
  const options = parseArgs();
  const services = resolveServices(manifest, options);
  const generatedScript = options.write ? writeStartScript(services) : undefined;
  const runResults = options.run ? services.map(runService) : [];

  return {
    ok: true,
    service: options.service ?? "all",
    generatedScript,
    services: services.map((service) => ({
      id: service.id,
      label: service.label,
      env: service.env,
      runtime: service.runtime ?? null,
      startCommand: service.startCommand
    })),
    runResults
  };
}

function printReport(report) {
  console.log("Learn English local speech starter");
  console.log("");

  if (report.generatedScript) {
    console.log(`Generated: ${report.generatedScript}`);
    console.log("");
  }

  report.services.forEach((service) => {
    console.log(`${service.label}`);
    console.log(`  ${service.runtime?.command ? `Command: ${service.runtime.command}` : `Manual: ${service.startCommand}`}`);
  });

  if (report.runResults.length > 0) {
    console.log("");
    report.runResults.forEach((item) => {
      console.log(`${item.action} ${item.id}: ${item.detail}`);
    });
  }
}

const options = parseArgs();
const report = createReport();

if (options.json) {
  console.log(JSON.stringify(report, null, 2));
} else {
  printReport(report);
}
